const path = require("node:path");
const fs = require("node:fs");

const lucide = require(path.join(
  __dirname,
  "..",
  "node_modules",
  "lucide",
  "dist",
  "cjs",
  "lucide.js",
));

const ICONS = [
  "Home",
  "ChartNoAxesColumn",
  "Keyboard",
  "Settings2",
  "ShieldCheck",
  "Cloud",
  "SlidersHorizontal",
  "FileCode2",
  "BookOpen",
  "Info",
  "Sun",
  "Moon",
  "Monitor",
  "Eye",
  "EyeOff",
  "Copy",
  "Trash2",
  "RefreshCw",
  "X",
  "CircleDot",
  "Sparkles",
];

const attrs = {
  xmlns: "http://www.w3.org/2000/svg",
  width: "24",
  height: "24",
  viewBox: "0 0 24 24",
  fill: "none",
  stroke: "currentColor",
  "stroke-width": "2",
  "stroke-linecap": "round",
  "stroke-linejoin": "round",
};

function toSvgString(elements) {
  const inner = elements
    .map(([tag, a]) => {
      const pairs = Object.entries(a)
        .map(([k, v]) => `${k}="${v}"`)
        .join(" ");
      return `<${tag} ${pairs}/>`;
    })
    .join("");
  const attrStr = Object.entries(attrs)
    .map(([k, v]) => `${k}="${v}"`)
    .join(" ");
  return `<svg ${attrStr}>${inner}</svg>`;
}

const entries = [];
for (const name of ICONS) {
  const iconData = lucide[name];
  if (!iconData) {
    console.warn(`Icon not found: ${name}`);
    continue;
  }
  const kebab = name
    .replace(/([A-Z])/g, (_m, p, i) => (i ? "-" : "") + p.toLowerCase())
    .replace(/(\d+)/g, "-$1");
  entries.push(`  "${kebab}": '${toSvgString(iconData)}'`);
}

const output = `// Auto-generated by build/extract-icons.js — do not edit\nwindow.LucideIcons = {\n${entries.join(",\n")}\n};\n`;

const outPath = path.join(__dirname, "..", "renderer", "lucide-icons.js");
fs.writeFileSync(outPath, output, "utf-8");
console.log(`Extracted ${entries.length} icons to ${outPath}`);
